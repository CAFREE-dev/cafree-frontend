// UIColor
// Extension
// 사용할 때는

import Foundation
import UIKit

extension UIColor {
    class var primary: UIColor? { return UIColor(named: "Primary") }        // .primary
    class var secondary: UIColor? { return UIColor(named: "Secondary") }    // .secondary
    class var point: UIColor? { return UIColor(named: "Point") }            // .point
    
}
